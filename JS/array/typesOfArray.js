// Types of Arrays in JavaScript:

// While JavaScript doesn't have explicit array types like in some other languages, arrays can be categorized based on their content and usage:

// 1. Numeric Arrays:

// Primarily store numbers.
// Examples:

// const numbers = [1, 2, 3, 4, 5];
// const decimalNumbers = [1.2, 3.4, 5.6];

// 2. String Arrays:

// Store strings (textual data).
// Examples:

// const fruits = ["apple", "banana", "orange"];
// const names = ["Alice", "Bob", "Charlie"];

// 3. Mixed Arrays:

// Store elements of different data types.
// Examples:

// const mixedArray = [10, "Hello", true, { name: "Alice" }];

// 4. Multidimensional Arrays:

// Arrays that contain other arrays as elements, creating a nested structure.
// Examples:

// const matrix = [
//     [1, 2, 3],
//     [4, 5, 6],
//     [7, 8, 9]
//   ];

// 5. Associative Arrays (Objects):

// While not technically arrays, objects are often used to store key-value pairs, similar to associative arrays in other languages.
// Examples:

// const person = {
//     name: "Alice",
//     age: 30,
//     city: "New York"
// };